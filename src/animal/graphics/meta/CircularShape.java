package animal.graphics.meta;

public interface CircularShape {
	/**
	 * Returns the radius of the circular shape as an int
	 * 
	 * @return the radius of this shape as an int value
	 */
  public int getRadius();
  
	/**
	 * Set the radius of the circular shape
	 * 
	 * @param r the target radius for the circular shape
	 */
  public void setRadius(int r);
} // CircularShape
