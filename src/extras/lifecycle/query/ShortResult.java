package extras.lifecycle.query;

/**
 * This is a simply short answer to a <code>Question</code>
 * There are three possible short answers:
 * <ul>
 *  <li>Yes, positive answer</li>
 *  <li>No, negative answer</li>
 *  <li>Unknown, which may also be seen as "I don't know"</li>
 * </ul>
 * 
 * @author Mihail Mihaylov
 *
 */
public enum ShortResult {
    YES,
    NO,
    UNKNOWN;
}
